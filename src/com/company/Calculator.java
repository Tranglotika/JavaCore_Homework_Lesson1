package com.company;

public class Calculator {
    public void display(int x, int y) {
        System.out.println("Cộng 2 số bằng: " + (x + y));
        System.out.println("Trừ 2 số bằng: " + (x - y));
        System.out.println("Nhân 2 số bằng: " + (x * y));
        System.out.println("Chia 2 số bằng: " + (x / y));
    }
}
